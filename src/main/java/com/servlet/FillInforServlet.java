package com.servlet;

import java.io.IOException;
import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 *
 * @author Administrator
 */
@WebServlet("/fill-infor")
public class FillInforServlet extends HttpServlet {

    @Override
    protected void doPost(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        resp.setContentType("text/html");
        resp.setCharacterEncoding("UTF-8");
        req.setCharacterEncoding("UTF-8");

        String phoneNumber = req.getParameter("phoneNumber");
        String address = req.getParameter("address");

        req.getSession().setAttribute("phone", phoneNumber);
        req.getSession().setAttribute("address", address);

        req.getSession().setAttribute("infor", "fill");
        resp.sendRedirect("/cart-list");
    }
}
